# Function to convert the raw number back into your survey's exact categories
def categorize_time(mins):
    if pd.isna(mins):
        return np.nan
    elif mins < 15:
        return 'Less than 15'
    elif mins <= 30:
        return '15-30'
    elif mins <= 45:
        return '31-45'
    elif mins <= 60:
        return '46-60'
    else:
        return 'More than 60'

# Apply the function and fill the missing blanks in the original column
