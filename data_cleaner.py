#cleaning area issue
def clean_area(area):
    # Skip null values if they exist
    if pd.isna(area):
        return area

    # Convert to lowercase to ignore capitalization
    a_lower = area.lower().strip()

    if 'motijheel' in a_lower:
        return 'Motijheel'

    if 'mohakhali' in a_lower:
        return 'Mohakhali Intersection'

    if a_lower == 'south':
        return 'Uttara South'

    if 'uttara' in a_lower or 'uttor' in a_lower or a_lower == 'north':
        return 'Uttara North'

    return area

